package Aufgabe2;

public class Artikel {

    protected String name;
    protected int preis;

    public String getName() {
        return name;
    }

    public int getPreis() {
        return preis;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPreis(int preis) {
        this.preis = preis;
    }

}
